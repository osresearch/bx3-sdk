/** ---------------------------------------------------------------------------
 * @brief   :
 * @note    :
 * @param   :
 * @param   :
 * @retval  :
-----------------------------------------------------------------------------*/
static void prv_sum( char argc, char * argv )
{
    int arg;
    int sum = 0;
    for( uint8_t i = 1; i < argc ; i++ ) {
        sscanf( &( argv[argv[i]] ), "%d", &arg );
        sum += arg;
    }
    printf( "sum:%d\r\n", sum );
}
/*========================= end of private function ==========================*/


