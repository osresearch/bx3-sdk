void uart_shell_test( void )
{
    bxs_init();

    /* add user cmd */

    if( !bxs_add_cmd( "sum", prv_sum, "calculate the sum of paras " ) ) {
        printf( "add cmd fail\r\n" );
        return ;
    }

    /* end of add user cmd */
    while( 1 ) {
        bxs_run();
    }
}
